// ── Helpers ────────────────────────────────────────────────────────

[[maybe_unused]] static Packet make_pkt(std::initializer_list<uint8_t> bytes) {
    Packet p{};
    std::copy(bytes.begin(), bytes.end(), p.begin());
    return p;
}

static void pack_u16_le(Packet& p, int offset, uint16_t val) {
    p[offset]     = val & 0xFF;
    p[offset + 1] = (val >> 8) & 0xFF;
}

uint16_t float_to_u16(float f) {
    f = std::clamp(f, -1.0f, 1.0f);
    int v = static_cast<int>(std::round(NEUTRAL_U16 + f * 0x7FFF));
    return static_cast<uint16_t>(std::clamp(v, 0, 0xFFFF));
}

