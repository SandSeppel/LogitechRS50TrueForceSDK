// ── Core packet builder ────────────────────────────────────────────

Packet build_packet(
    uint8_t  reg,
    float    kinesthetic,
    float    tf_amp,
    float    tf_freq,
    int      sample_idx,
    uint8_t  tf_gain,
    std::function<float(float)> waveform_fn)
{
    Packet pkt{};
    pkt[0] = 0x01;
    pkt[4] = 0x01;
    pkt[5] = reg;

    // Kinesthetic (bytes 6-9)
    if (kinesthetic == 0.0f) {
        pkt[6] = 0x00; pkt[7] = 0x80;
        pkt[8] = 0x00; pkt[9] = 0x80;
    } else {
        uint16_t kv = float_to_u16(kinesthetic);
        pack_u16_le(pkt, 6, kv);
        pack_u16_le(pkt, 8, kv);
    }

    // TrueForce header
    pkt[10] = static_cast<uint8_t>(SHIFT_PER_PKT);
    pkt[11] = tf_gain;

    // Fill all 13 pairs.
    // Pair j → absolute sample index = sample_idx - (PAIRS_PER_PKT-1) + j
    //   j=0  → oldest  (bytes 12-15)
    //   j=12 → newest  (bytes 60-63)
    const double tau = 2.0 * M_PI;
    for (int j = 0; j < PAIRS_PER_PKT; ++j) {
        int abs_s = sample_idx - (PAIRS_PER_PKT - 1) + j;
        uint16_t val = NEUTRAL_U16;
        if (tf_amp != 0.0f) {
            double t   = static_cast<double>(abs_s) / SAMPLE_RATE;
            float  raw;
            if (waveform_fn) {
                raw = std::clamp(waveform_fn(static_cast<float>(t)), -1.0f, 1.0f) * tf_amp;
            } else {
                raw = static_cast<float>(std::sin(tau * tf_freq * t)) * tf_amp;
            }
            int v = static_cast<int>(0x8000 + raw * 0x7FFF);
            val   = static_cast<uint16_t>(std::clamp(v, 0, 0xFFFF));
        }
        pack_u16_le(pkt, 12 + j * 4,     val);
        pack_u16_le(pkt, 12 + j * 4 + 2, val);
    }

    return pkt;
}

