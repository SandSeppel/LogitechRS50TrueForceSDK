// Helper: build a command packet from hex byte list
static Packet cmd(uint8_t cmd_id, uint8_t slot, std::initializer_list<uint8_t> payload) {
    Packet p{};
    p[0] = 0x01;
    p[4] = cmd_id;
    p[5] = slot;
    int i = 6;
    for (uint8_t b : payload) p[i++] = b;
    return p;
}

